package javabeans;

public class GoodsInfo {
	private int goods_no;
	private String goods_name;
	private String goods_fresh;
	private String goods_factory;
	private float goods_price;
	public int getGoods_no() {
		return goods_no;
	}
	public void setGoods_no(int goods_no) {
		this.goods_no = goods_no;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getGoods_fresh() {
		return goods_fresh;
	}
	public void setGoods_fresh(String goods_fresh) {
		this.goods_fresh = goods_fresh;
	}
	public String getGoods_factory() {
		return goods_factory;
	}
	public void setGoods_factory(String goods_factory) {
		this.goods_factory = goods_factory;
	}
	public float getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(float goods_price) {
		this.goods_price = goods_price;
	}
}
