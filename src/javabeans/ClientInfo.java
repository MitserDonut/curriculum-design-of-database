package javabeans;

public class ClientInfo {
	private int clt_no;
	private String clt_name;
	private String clt_type;
	private String clt_tele;
	private String clt_sex;
	private String clt_bz;
	public int getClt_no() {
		return clt_no;
	}
	public void setClt_no(int clt_no) {
		this.clt_no = clt_no;
	}
	public String getClt_name() {
		return clt_name;
	}
	public void setClt_name(String clt_name) {
		this.clt_name = clt_name;
	}
	public String getClt_type() {
		return clt_type;
	}
	public void setClt_type(String clt_type) {
		this.clt_type = clt_type;
	}
	public String getClt_tele() {
		return clt_tele;
	}
	public void setClt_tele(String clt_tele) {
		this.clt_tele = clt_tele;
	}
	public String getClt_sex() {
		return clt_sex;
	}
	public void setClt_sex(String clt_sex) {
		this.clt_sex = clt_sex;
	}
	public String getClt_bz() {
		return clt_bz;
	}
	public void setClt_bz(String clt_bz) {
		this.clt_bz = clt_bz;
	}
}
