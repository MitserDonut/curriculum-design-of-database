package javabeans;

public class WorkerInfo {
	private int wk_no;
	private String wk_name;
	private String wk_sex;
	private String wk_tete;
	private int wk_place;
	
	public int getWk_no() {
		return wk_no;
	}
	public void setWk_no(int wk_no) {
		this.wk_no = wk_no;
	}
	public String getWk_name() {
		return wk_name;
	}
	public void setWk_name(String wk_name) {
		this.wk_name = wk_name;
	}
	public String getWk_sex() {
		return wk_sex;
	}
	public void setWk_sex(String wk_sex) {
		this.wk_sex = wk_sex;
	}
	public String getWk_tete() {
		return wk_tete;
	}
	public void setWk_tete(String wk_tete) {
		this.wk_tete = wk_tete;
	}
	public int getWk_place() {
		return wk_place;
	}
	public void setWk_place(int wk_place) {
		this.wk_place = wk_place;
	}
}
