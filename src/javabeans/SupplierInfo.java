package javabeans;

public class SupplierInfo {
	private int sup_no;
	private String sup_name;
	private String sup_type;
	private String sup_tele;
	private String sup_sex;
	private String sup_bz;
	
	public int getSup_no() {
		return sup_no;
	}
	public void setSup_no(int sup_no) {
		this.sup_no = sup_no;
	}
	public String getSup_name() {
		return sup_name;
	}
	public void setSup_name(String sup_name) {
		this.sup_name = sup_name;
	}
	public String getSup_type() {
		return sup_type;
	}
	public void setSup_type(String sup_type) {
		this.sup_type = sup_type;
	}
	public String getSup_tele() {
		return sup_tele;
	}
	public void setSup_tele(String sup_tele) {
		this.sup_tele = sup_tele;
	}
	public String getSup_sex() {
		return sup_sex;
	}
	public void setSup_sex(String sup_sex) {
		this.sup_sex = sup_sex;
	}
	public String getSup_bz() {
		return sup_bz;
	}
	public void setSup_bz(String sup_bz) {
		this.sup_bz = sup_bz;
	}
}
